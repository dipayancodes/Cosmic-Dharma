// Fast search: returns top matches sorted by relevance
export function searchCities(query: string, limit: number = 10): Array<{name: string; lat: number; lng: number; tz: number}> {
  if (!query || query.length < 2) return [];
  const q = query.toLowerCase();
  const results: Array<{name: string; lat: number; lng: number; tz: number; score: number}> = [];
  
  for (const [name, lat, lng, tz] of CITIES) {
    const lower = name.toLowerCase();
    let score = 0;
    if (lower.startsWith(q)) score = 100;
    else if (lower.includes(q)) score = 50;
    else {
      // Check individual words
      const words = lower.split(/[,\s]+/);
      for (const w of words) {
        if (w.startsWith(q)) { score = 70; break; }
        if (w.includes(q)) { score = 30; break; }
      }
    }
    if (score > 0) results.push({ name, lat, lng, tz, score });
  }
  
  results.sort((a, b) => b.score - a.score);
  return results.slice(0, limit).map(({ name, lat, lng, tz }) => ({ name, lat, lng, tz }));
}
